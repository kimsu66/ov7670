module ov7670_init_rom(
    input  wire [7:0] addr,
    output reg [15:0] data,
    output reg        last
);

    always @(*) begin
        last = 1'b0;

        case (addr)

            // ---------------------------------------------
            // 1. 소프트 리셋
            // ---------------------------------------------
            // 8'd0:  data = {8'h12, 8'b1000_0000}; // COM7  [7]=reset
            8'd0:  data = {8'h12, 8'b0001_0100}; // COM7  [7]=reset

            // ---------------------------------------------
            // 2. 출력 포맷
            // ---------------------------------------------
            8'd1:  data = {8'h12, 8'b0001_0100}; // COM7  [5]=QVGA [2]=RGB
            8'd2:  data = {8'h8C, 8'b0000_0000}; // RGB444 [1]=rgb444_en : 비활성
            8'd3:  data = {8'h40, 8'b1101_0000}; // COM15 [7:6]=출력범위(11=풀레인지) [5:4]=포맷(10=RGB565)
            8'd4:  data = {8'h3A, 8'b0000_0000}; // TSLB  [4]=out_seq [3]=uv_sat [2]=yuv_seq
            8'd5:  data = {8'h04, 8'b0000_0000}; // COM1  [6]=ccir656 : 비활성

            // ---------------------------------------------
            // 3. 클럭 설정
            // ---------------------------------------------
            8'd6:  data = {8'h11, 8'b0000_0000}; // CLKRC [7]=pll_bypass [5:0]=prescaler : 프리스케일 없음
            8'd7:  data = {8'h3E, 8'b0001_1001}; // COM14 [4]=manual [3]=dcw_pclk_en [2:0]=pclk_div(001=÷2)
            8'd8:  data = {8'h73, 8'b0000_0000}; // SCALING_PCLK_DIV [3:0]=div(0000=bypass)
            8'd9:  data = {8'hA2, 8'b0000_0010}; // SCALING_PCLK_DELAY : 딜레이=2

            // ---------------------------------------------
            // 4. 스케일링 (QVGA DCW)
            // ---------------------------------------------
            8'd10: data = {8'h0C, 8'b0000_0100}; // COM3  [3]=scale_en [2]=dcw_en : DCW 활성
            8'd11: data = {8'h70, 8'b0011_1010}; // SCALING_XSC  [7]=test_pattern [6:0]=scale_factor
            8'd12: data = {8'h71, 8'b0011_0101}; // SCALING_YSC  [7]=test_pattern [6:0]=scale_factor
            8'd13: data = {8'h72, 8'b0001_0001}; // SCALING_DCWCTR [7:4]=vert [3:0]=horiz (0001=÷2)

            // ---------------------------------------------
            // 5. 윈도우 / 타이밍
            // ---------------------------------------------
            8'd14: data = {8'h17, 8'b0001_0110}; // HSTART
            8'd15: data = {8'h18, 8'b0000_0100}; // HSTOP
            8'd16: data = {8'h32, 8'b0010_0100}; // HREF  [7:3]=hstop_lsb3 [2:0]=hstart_lsb3
            8'd17: data = {8'h19, 8'b0000_0010}; // VSTRT
            8'd18: data = {8'h1A, 8'b0111_1010}; // VSTOP
            8'd19: data = {8'h03, 8'b0000_1010}; // VREF  [7:6]=vstop_lsb2 [5:4]=vstrt_lsb2
            8'd20: data = {8'h15, 8'b0000_0000}; // COM10 [7]=hsync_as_href [5]=pclk_rev [4]=href_rev [3]=vsync_rev

            // ---------------------------------------------
            // 6. 화질 설정
            // ---------------------------------------------
            8'd21: data = {8'h14, 8'b0000_0000}; // COM9  [6:4]=agc_ceil(000=2x)
            8'd22: data = {8'h3D, 8'b1000_0000}; // COM13 [7]=gamma_en [6]=uv_auto [0]=uv_swap
            8'd23: data = {8'h4F, 8'b1000_0000}; // MTX1
            8'd24: data = {8'h50, 8'b0011_0000}; // MTX2
            8'd25: data = {8'h51, 8'b0000_0000}; // MTX3
            8'd26: data = {8'h52, 8'b0011_0010}; // MTX4
            8'd27: data = {8'h53, 8'b0101_1110}; // MTX5
            8'd28: data = {8'h54, 8'b1000_0000}; // MTX6
            8'd29: data = {8'h58, 8'b1001_1110}; // MTXS  [7]=auto_contrast [5:0]=matrix_sign
            8'd30: begin
                data = {8'h13, 8'b1000_0101}; // COM8
                last = 1'b1;
            end
        endcase
    end

endmodule
